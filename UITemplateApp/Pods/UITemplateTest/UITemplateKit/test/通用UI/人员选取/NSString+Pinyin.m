char pinyinFirstLetter(unsigned short hanzi) {
    int index = hanzi - HANZI_START;
    if (index >= 0 && index <= HANZI_COUNT) {
        return firstLetterArray[index];
    } else {
        return hanzi;
    }
}

@implementation NSString (Pinyin)

- (NSString *)firstPinyinLettersForHans {
    NSMutableString *firstPinyinLetters = [@"" mutableCopy];
    for (int i=0; i<self.length; i++) {
        NSString *singlePinyinLetter = [[NSString stringWithFormat:@"%c",pinyinFirstLetter([self characterAtIndex:i])]uppercaseString];
        if (singlePinyinLetter.length > 0) {
            [firstPinyinLetters appendString:singlePinyinLetter];
        }
    }

    return firstPinyinLetters.length > 0 ? firstPinyinLetters : nil;
}

- (NSString *)firstPinyinLetterForHans {
    NSString *singlePinyinLetter = [[NSString stringWithFormat:@"%c",pinyinFirstLetter([self characterAtIndex:0])]uppercaseString];

    return singlePinyinLetter.length > 0 ? singlePinyinLetter : nil;
}

